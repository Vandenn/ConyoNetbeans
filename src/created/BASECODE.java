abstract class PLACEHOLDER_ABSTRACT implements iNode 
{
    public static class PLACEHOLDER_STATIC extends PLACEHOLDER_ABSTRACT
    {
        public PLACEHOLDER_TYPE PLACEHOLDER_VARA;
        
        public PLACEHOLDER_STATIC(PLACEHOLDER_TYPE PLACEHOLDER_VARA)
        {
            this.PLACEHOLDER_VARA = PLACEHOLDER_VARA;
        }
        
        public String toString()
        {
            return PLACEHOLDER_VARA.toString();
        }
    }
    
}